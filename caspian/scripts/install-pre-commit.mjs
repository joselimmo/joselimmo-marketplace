#!/usr/bin/env node
// Pre-commit hook installer for the Caspian sub-folder repo arrangement.
//
// Background: caspian/ is a sub-folder of joselimmo-marketplace-bmad/, not
// the root of an independent git repository. The actual `.git/` lives at the
// marketplace root, so simple-git-hooks's default auto-detect (which expects
// `.git/` next to package.json) writes the hook at the wrong location and
// silently creates a spurious `caspian/.git/` directory that git never
// consults.
//
// This installer reads the `simple-git-hooks` config from caspian/package.json,
// resolves the real git-dir via `git rev-parse --git-dir`, and writes the hook
// directly there. Idempotent — safe to run on every `pnpm install` (via the
// `prepare` script).
//
// On a tarball install (no git history), `git rev-parse` errors and we exit 0
// silently — same UX as `simple-git-hooks || true`.

import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const pkgJsonPath = path.resolve(here, "..", "package.json");

let gitDir;
try {
  gitDir = execSync("git rev-parse --git-dir", {
    cwd: path.resolve(here, ".."),
    stdio: ["ignore", "pipe", "ignore"],
  })
    .toString()
    .trim();
} catch {
  // Not a git repo (e.g., npm tarball install). Nothing to wire.
  process.exit(0);
}

const absGitDir = path.isAbsolute(gitDir)
  ? gitDir
  : path.resolve(here, "..", gitDir);
const hooksDir = path.join(absGitDir, "hooks");

if (!fs.existsSync(hooksDir)) {
  fs.mkdirSync(hooksDir, { recursive: true });
}

const pkg = JSON.parse(fs.readFileSync(pkgJsonPath, "utf8"));
const hooks = pkg["simple-git-hooks"] ?? {};

const HOOK_HEADER = [
  "#!/bin/sh",
  "# Generated by caspian/scripts/install-pre-commit.mjs",
  "# Source: caspian/package.json `simple-git-hooks` block",
  "",
  'if [ "$SKIP_SIMPLE_GIT_HOOKS" = "1" ]; then',
  '  echo "[INFO] SKIP_SIMPLE_GIT_HOOKS=1, skipping hook."',
  "  exit 0",
  "fi",
  "",
].join("\n");

let written = 0;
for (const [hookName, command] of Object.entries(hooks)) {
  if (typeof command !== "string" || command.length === 0) continue;
  const target = path.join(hooksDir, hookName);
  const body = `${HOOK_HEADER}${command}\n`;
  fs.writeFileSync(target, body, { mode: 0o755 });
  fs.chmodSync(target, 0o755);
  console.log(`[install-pre-commit] wired ${hookName} → ${target}`);
  written += 1;
}

// Cleanup: simple-git-hooks may have left a spurious caspian/.git/ from a
// previous install. Remove it so git's `rev-parse` consistently resolves to
// the marketplace root and so the spurious directory doesn't confuse readers.
const spurious = path.resolve(here, "..", ".git");
if (
  spurious !== absGitDir &&
  fs.existsSync(spurious) &&
  fs.statSync(spurious).isDirectory()
) {
  // Only remove if it looks spurious: contains only a `hooks/` subdir.
  const entries = fs.readdirSync(spurious);
  if (entries.length === 1 && entries[0] === "hooks") {
    fs.rmSync(spurious, { recursive: true, force: true });
    console.log(`[install-pre-commit] removed spurious ${spurious}`);
  }
}

if (written === 0) {
  console.log(
    "[install-pre-commit] no hooks declared in simple-git-hooks block",
  );
}
